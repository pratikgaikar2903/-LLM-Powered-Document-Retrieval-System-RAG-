from langchain_core.prompts import PromptTemplate
# StrOutputParser: Parses the LLM's output into a simple string.
from langchain_core.output_parsers import StrOutputParser
# RunnablePassthrough: Allows input to pass through directly in an LCEL chain.
from langchain_core.runnables import RunnablePassthrough     # ✅ replaces create_retrieval_chain

