class RAGSystem:
    """
    A class to encapsulate the Retrieval Augmented Generation (RAG) system.
    Handles document ingestion, vector store management, and querying using an LLM.
    """
    def __init__(
        self,
        file_path: str = "/content/Pratik Gaikar - ATS Data Resume.pdf",
        persist_dir: str = "/content/chroma_db"
    ):
        """
        Initializes the RAGSystem with a document path and a persistence directory for ChromaDB.
        Loads embeddings and the LLM, and attempts to load an existing vector store.
        """
        self.file_path = file_path
        self.persist_dir = persist_dir
        self.vector_store = None

        print("Loading HuggingFace embeddings (all-MiniLM-L6-v2)...")
        # Initialize HuggingFace embeddings for text conversion to vectors.
        self.embeddings = HuggingFaceEmbeddings(model_name="all-MiniLM-L6-v2")

        print("Initializing Groq LLM (llama-3.1-8b-instant)...")
        # Initialize the Groq Chat model with a specific model name and temperature.
        self.llm = ChatGroq(model_name="llama-3.1-8b-instant", temperature=0)

        # Load existing vector store if available
        # Checks if the persistence directory exists and contains data to load the ChromaDB.
        if os.path.exists(persist_dir) and os.listdir(persist_dir):
            print("✅ Found existing vector store — loading it.")
            self.vector_store = Chroma(
                persist_directory=persist_dir,
                embedding_function=self.embeddings
            )
        else:
            print("⚠️  No vector store found. Call ingest_documents() to build one.")

    def ingest_documents(self):
        """Load PDF → chunk → embed → persist to Chroma."""
        # Ensures the PDF file exists before proceeding.
        if not os.path.exists(self.file_path):
            raise FileNotFoundError(f"PDF not found at: {self.file_path}")

        print(f"📄 Loading: {self.file_path}")
        # Load the PDF document using PyPDFLoader.
        loader = PyPDFLoader(self.file_path)
        documents = loader.load()
        print(f"   Loaded {len(documents)} page(s).")

        # Split the loaded documents into smaller chunks for effective retrieval.
        # `chunk_size`: Maximum size of each text chunk.
        # `chunk_overlap`: Overlap between consecutive chunks to maintain context.
        splitter = RecursiveCharacterTextSplitter(chunk_size=1000, chunk_overlap=200)
        chunks = splitter.split_documents(documents)
        print(f"   Split into {len(chunks)} chunks.")

        print("🔢 Embedding and storing in Chroma...")
        # Create a new Chroma vector store from the document chunks and embeddings.
        # The vector store is persisted to the specified directory.
        self.vector_store = Chroma.from_documents(
            documents=chunks,
            embedding=self.embeddings,
            persist_directory=self.persist_dir
        )
        print("✅ Ingestion complete.")

    def query(self, question: str) -> str:
        """Run a question through the RAG pipeline using LCEL (modern approach)."""
        # Checks if the vector store has been initialized before attempting to query.
        if self.vector_store is None:
            raise RuntimeError("Vector store is empty. Run ingest_documents() first.")

        # Configure the retriever to fetch the top 3 most relevant documents.
        retriever = self.vector_store.as_retriever(search_kwargs={"k": 3})

        # Define the prompt template for the LLM, instructing it to use the provided context.
        prompt = PromptTemplate.from_template("""
You are a helpful assistant. Answer the question using ONLY the context below.
If the answer is not in the context, say "I don't have enough information."

Context:
{context}

Question: {question}

Answer:""")

        # ✅ Modern LCEL chain — replaces create_retrieval_chain entirely.
        # This function formats the retrieved documents into a single string.
        def format_docs(docs):
            return "\n\n".join(doc.page_content for doc in docs)

        # Construct the RAG chain using LangChain Expression Language (LCEL).
        # 1. Retrieve documents based on the question.
        # 2. Format the retrieved documents.
        # 3. Combine with the question into the prompt.
        # 4. Pass the prompt to the LLM.
        # 5. Parse the LLM's output.
        chain = (
            {"context": retriever | format_docs, "question": RunnablePassthrough()}
            | prompt
            | self.llm
            | StrOutputParser()
        )

        print(f"\n🔍 Question: {question}")
        # Invoke the RAG chain with the user's question.
        answer = chain.invoke(question)
        return answer


# --- Run ---
# Instantiate the RAGSystem class.
rag = RAGSystem()

# If no vector store is found, ingest documents to build one.
if rag.vector_store is None:
    rag.ingest_documents()

# Example query to the RAG system.
answer = rag.query("What are the main skills or objectives mentioned in the resume?")
print("\n📝 Answer:\n", answer)